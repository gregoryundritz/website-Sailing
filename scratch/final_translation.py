def translate_html(source_path, target_path, lang_code, text_replacements, seo_replacements):
    with open(source_path, 'r', encoding='utf-8') as f:
        content = f.read()

    # Apply SEO replacements
    for old, new in seo_replacements:
        content = content.replace(old, new)
        
    # Language active switcher
    if lang_code == 'en':
        content = content.replace('lang-active">FR</a> <a href="/de/">DE</a> <a href="/en/">EN',
                                  '">FR</a> <a href="/de/">DE</a> <a href="/en/" class="lang-active">EN')
    elif lang_code == 'de':
        content = content.replace('lang-active">FR</a> <a href="/de/">DE</a> <a href="/en/">EN',
                                  '">FR</a> <a href="/de/" class="lang-active">DE</a> <a href="/en/">EN')

    # Ensure hidden forms get right subject
    content = content.replace('value="Nouvelle demande de réservation (FR)"', f'value="New booking request ({lang_code.upper()})"')

    # Apply all text translations
    for old, new in text_replacements:
        content = content.replace(old, new)
        
    with open(target_path, 'w', encoding='utf-8') as f:
        f.write(content)


